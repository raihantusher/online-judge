    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $post=new Post();

        $post->title=$request->title;
        $post->body=$request->body;
        $post->category_id=$request->cat_id;

        $post->user_id=Auth::id();

        $post->save();

        //sending mail to the current user
        //Mail::to("raihan.tusher@yahoo.com")->send(new PostCreated($post));
        Mail::to(Auth::user()->email)->send(new PostCreated($post));

        session()->flash('msg', 'Post has been created successfully!');
        return redirect()->back();
        
    }

